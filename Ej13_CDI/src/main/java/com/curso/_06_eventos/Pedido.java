package com.curso._06_eventos;

public class Pedido {
	private String codigo;
	
	public Pedido() {
		super();
	}

	public Pedido(String codigo) {
		super();
		this.codigo = codigo;
	}

	public String getCodigo() {
		return codigo;
	}

	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}
}
