package com.curso.modelo.entidad;

public class Pelicula {

	private String titulo;

	public Pelicula() {
		super();
	}

	public Pelicula(String titulo) {
		super();
		this.titulo = titulo;
	}

	public String getTitulo() {
		return titulo;
	}

	public void setTitulo(String titulo) {
		this.titulo = titulo;
	}
	
	
	
}
